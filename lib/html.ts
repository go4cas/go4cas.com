// Tiny HTML/XML helpers shared by templates and the RSS generator.

/** Escape text for safe interpolation into HTML/XML text nodes and attributes. */
export function esc(value: unknown): string {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
